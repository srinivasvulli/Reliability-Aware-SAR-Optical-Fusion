def forward_any(model, name, radar, optical):
    if name == "radar":
        return model(radar)
    if name == "optical":
        return model(optical)
    return model(radar, optical)

def train_epoch(model, name, loader, optimizer, criterion):
    model.train()
    total = 0.0
    aux = 0.45 if name == "proposed" else (0.35 if name == "logit_fusion" else 0.0)
    for radar, optical, labels in tqdm(loader, leave=False):
        radar, optical, labels = radar.to(DEVICE), optical.to(DEVICE), labels.to(DEVICE)
        if name == "proposed":
            optical = train_degradation_proposed(optical)
        elif name in ["early_fusion", "feature_fusion", "logit_fusion"]:
            optical = train_degradation_fusion_baseline(optical)
        optimizer.zero_grad(set_to_none=True)
        if name in GATED:
            fused, w, rl, ol = model(radar, optical, return_all=True)
            loss = criterion(fused, labels) + aux * criterion(rl, labels) + aux * criterion(ol, labels)
        else:
            loss = criterion(forward_any(model, name, radar, optical), labels)
        loss.backward()
        optimizer.step()
        total += loss.item() * labels.shape[0]
    return total / len(loader.dataset)

@torch.no_grad()
def evaluate(model, name, loader, setting="clean"):
    """Reproducible evaluation: degradation draws are seeded per setting."""
    model.eval()
    torch.manual_seed(EVAL_SEED[setting])
    correct, total, weights = 0, 0, []
    for radar, optical, labels in tqdm(loader, leave=False):
        radar, optical, labels = radar.to(DEVICE), optical.to(DEVICE), labels.to(DEVICE)
        optical = degrade_optical(optical, setting)
        if name in GATED:
            logits, w, _, _ = model(radar, optical, return_all=True)
            weights.append(w.cpu())
        else:
            logits = forward_any(model, name, radar, optical)
        correct += (logits.argmax(dim=1) == labels).sum().item()
        total += labels.numel()
    out = {"accuracy": correct / total}
    if weights:
        w = torch.cat(weights)
        out["avg_radar_weight"] = w[:, 0].mean().item()
        out["avg_optical_weight"] = w[:, 1].mean().item()
    return out

