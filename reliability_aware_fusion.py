class SmallEncoder(nn.Module):
    def __init__(self, in_channels, feature_dim=128):
        super().__init__()
        self.net = nn.Sequential(
            nn.Conv2d(in_channels, 32, 3, padding=1), nn.BatchNorm2d(32),
            nn.ReLU(inplace=True), nn.MaxPool2d(2),
            nn.Conv2d(32, 64, 3, padding=1), nn.BatchNorm2d(64),
            nn.ReLU(inplace=True), nn.MaxPool2d(2),
            nn.Conv2d(64, 128, 3, padding=1), nn.BatchNorm2d(128),
            nn.ReLU(inplace=True), nn.AdaptiveAvgPool2d(1),
        )
        self.proj = nn.Linear(128, feature_dim)
    def forward(self, x):
        return self.proj(self.net(x).flatten(1))

class SingleModalityClassifier(nn.Module):
    def __init__(self, in_channels):
        super().__init__()
        self.encoder = SmallEncoder(in_channels)
        self.classifier = nn.Linear(128, NUM_CLASSES)
    def forward(self, x):
        return self.classifier(self.encoder(x))

class EarlyFusionClassifier(nn.Module):
    def __init__(self):
        super().__init__()
        self.encoder = SmallEncoder(4)
        self.classifier = nn.Linear(128, NUM_CLASSES)
    def forward(self, radar, optical):
        return self.classifier(self.encoder(torch.cat([radar, optical], dim=1)))

class FeatureFusionClassifier(nn.Module):          # feature-level fusion variant
    def __init__(self):
        super().__init__()
        self.radar_encoder = SmallEncoder(1)
        self.optical_encoder = SmallEncoder(3)
        self.gate = nn.Sequential(nn.Linear(256, 128), nn.ReLU(inplace=True),
                                  nn.Linear(128, 2), nn.Softmax(dim=1))
        self.classifier = nn.Linear(128, NUM_CLASSES)
    def forward(self, radar, optical):
        rf, of = self.radar_encoder(radar), self.optical_encoder(optical)
        w = self.gate(torch.cat([rf, of], dim=1))
        return self.classifier(w[:, 0:1] * rf + w[:, 1:2] * of)

class LogitFusionClassifier(nn.Module):            # logit-level fusion variant
    def __init__(self, feature_dim=128):
        super().__init__()
        self.radar_encoder = SmallEncoder(1, feature_dim)
        self.optical_encoder = SmallEncoder(3, feature_dim)
        self.radar_head = nn.Linear(feature_dim, NUM_CLASSES)
        self.optical_head = nn.Linear(feature_dim, NUM_CLASSES)
        self.gate = nn.Sequential(nn.Linear(feature_dim * 2, feature_dim),
                                  nn.ReLU(inplace=True), nn.Dropout(0.15),
                                  nn.Linear(feature_dim, 2), nn.Softmax(dim=1))
    def forward(self, radar, optical, return_all=False):
        rf, of = self.radar_encoder(radar), self.optical_encoder(optical)
        rl, ol = self.radar_head(rf), self.optical_head(of)
        w = self.gate(torch.cat([rf, of], dim=1))
        fused = w[:, 0:1] * rl + w[:, 1:2] * ol
        return (fused, w, rl, ol) if return_all else fused

def confidence_features(logits):
    probs = torch.softmax(logits, dim=1)
    top2 = torch.topk(probs, k=2, dim=1).values
    entropy = -(probs * probs.clamp_min(1e-8).log()).sum(dim=1, keepdim=True) / np.log(NUM_CLASSES)
    return torch.cat([top2[:, 0:1], top2[:, 0:1] - top2[:, 1:2], entropy], dim=1)

def optical_quality_features(optical):
    return torch.cat([
        optical.abs().mean(dim=(1, 2, 3)).view(-1, 1),
        optical.std(dim=(1, 2, 3)).view(-1, 1),
        (optical.abs() < 1e-6).float().mean(dim=(1, 2, 3)).view(-1, 1),
        (optical.abs() > 2.5).float().mean(dim=(1, 2, 3)).view(-1, 1),
    ], dim=1)

class ProposedFusionClassifier(nn.Module):         # reliability-aware fusion (proposed)
    def __init__(self, feature_dim=128):
        super().__init__()
        self.radar_encoder = SmallEncoder(1, feature_dim)
        self.optical_encoder = SmallEncoder(3, feature_dim)
        self.radar_head = nn.Linear(feature_dim, NUM_CLASSES)
        self.optical_head = nn.Linear(feature_dim, NUM_CLASSES)
        self.gate = nn.Sequential(
            nn.Linear(feature_dim * 2 + 10, 128), nn.ReLU(inplace=True), nn.Dropout(0.20),
            nn.Linear(128, 64), nn.ReLU(inplace=True),
            nn.Linear(64, 2), nn.Softmax(dim=1))
    def forward(self, radar, optical, return_all=False):
        rf, of = self.radar_encoder(radar), self.optical_encoder(optical)
        rl, ol = self.radar_head(rf), self.optical_head(of)
        gate_in = torch.cat([rf, of, confidence_features(rl),
                             confidence_features(ol), optical_quality_features(optical)], dim=1)
        w = self.gate(gate_in)
        fused = w[:, 0:1] * rl + w[:, 1:2] * ol
        return (fused, w, rl, ol) if return_all else fused

