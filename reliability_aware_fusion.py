def spatial_unit(path):
    stem = path.stem
    m = re.search(r"(ROIs\d+_[A-Za-z]+)", stem)
    roi = m.group(1) if m else "noROI"
    m2 = re.search(r"_s[12]_(\d+)_p\d+", stem)          # SEN12MS: ..._s1_<scene>_p<patch>
    if m2:
        scene = m2.group(1)
    else:                                                # fallback: second-to-last number in stem
        nums = re.findall(r"\d+", stem)
        scene = nums[-2] if len(nums) >= 2 else (nums[-1] if nums else "0")
    return f"{roi}_sc{scene}"

